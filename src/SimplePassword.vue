<template>
  <div class="po-password-strength-bar" :class="passwordClass"></div>
</template>

<script>
import { checkPassword } from './logic'
export default {
  name: "password-meter",
  props: {
    password: String
  },
  computed: {
    passwordClass() {
      if (!this.password) {
        return null
      }

      return {
        [checkPassword(this.password)]: true,
        scored: true
      }
    }
  }
};
</script>

